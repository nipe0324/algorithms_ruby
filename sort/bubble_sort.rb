def sort(array)
  (0..array.size-1).each do |i|
    (0..array.size-2-i).each do |j|
      array[j], array[j+1] = array[j+1], array[j] if array[j] > array[j+1]
    end
    # debug
    printf("%2d : %s\n", i, array)
  end
  array
end

array = [15, 9, 8, 1, 4, 11, 7, 12, 13, 6, 5, 3, 16, 2, 10, 14]
sort(array)

